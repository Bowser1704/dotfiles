      signs = {
        add = { text = "+" },
        change = { text = "~" },
        topdelete = { text = "‾" },
        delete = { text = "_" },
        changedelete = { text = "~" },
        untracked = { text = "┆" },
      },
      current_line_blame = true, -- Toggle with `:Gitsigns toggle_current_line_blame`
