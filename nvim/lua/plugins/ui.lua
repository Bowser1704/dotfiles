      vim.cmd([[colorscheme sonokai]])
    end,
    dependencies = {
      "nvim-lua/plenary.nvim",
      "nvim-tree/nvim-web-devicons", -- not strictly required, but recommended
    },
  },
  {
    "mhinz/vim-startify",
    event = "VimEnter",
    priority = 1000, -- make sure to load this before all the other start plugins
